package com.example.model;

public class Computadora2 {

	private int id;
	private int mother;
	private int micro;
	private int gpu;
	private int disco;
	private int fuente;
	private int ram1;
	private int ram2;
	private int ram3;
	private int ram4;
	public Computadora2(int id, int mother, int micro, int gpu, int disco, int fuente, int ram1, int ram2, int ram3,
			int ram4) {
		super();
		this.id = id;
		this.mother = mother;
		this.micro = micro;
		this.gpu = gpu;
		this.disco = disco;
		this.fuente = fuente;
		this.ram1 = ram1;
		this.ram2 = ram2;
		this.ram3 = ram3;
		this.ram4 = ram4;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getMother() {
		return mother;
	}
	public void setMother(int mother) {
		this.mother = mother;
	}
	public int getMicro() {
		return micro;
	}
	public void setMicro(int micro) {
		this.micro = micro;
	}
	public int getGpu() {
		return gpu;
	}
	public void setGpu(int gpu) {
		this.gpu = gpu;
	}
	public int getDisco() {
		return disco;
	}
	public void setDisco(int disco) {
		this.disco = disco;
	}
	public int getFuente() {
		return fuente;
	}
	public void setFuente(int fuente) {
		this.fuente = fuente;
	}
	public int getRam1() {
		return ram1;
	}
	public void setRam1(int ram1) {
		this.ram1 = ram1;
	}
	public int getRam2() {
		return ram2;
	}
	public void setRam2(int ram2) {
		this.ram2 = ram2;
	}
	public int getRam3() {
		return ram3;
	}
	public void setRam3(int ram3) {
		this.ram3 = ram3;
	}
	public int getRam4() {
		return ram4;
	}
	public void setRam4(int ram4) {
		this.ram4 = ram4;
	}

	
	
	
	
}
